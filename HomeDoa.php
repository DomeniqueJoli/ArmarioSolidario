<h3>Sobre a iniciativa Armário solidário</h3>
<p>
O Armário Solidário é uma iniciativa social que tem como missão transformar peças de roupa em bons gestos. Atuamos como uma ponte entre pessoas dispostas a doar e instituições que acolhem <br>
quem mais precisa, promovendo a solidariedade de forma prática e acessível. Acreditamos que roupas podem aquecer o corpo, mas também são capazes de transmitir dignidade, autoestima e carinho.  <br>
<br>

Nosso propósito é dar um novo destino a roupas, calçados e acessórios que estão em bom estado, mas sem uso. Conectamos esses itens a crianças, jovens, adultos e idosos em situação de  <br>
vulnerabilidade, por meio de parcerias com instituições sérias e comprometidas. Cada doação é distribuída com responsabilidade, sempre respeitando a individualidade e a necessidade de quem recebe.  <br>

<br>
Mais do que doar, o Armário Solidário busca criar uma cultura de empatia, reaproveitamento e consciência social. Queremos inspirar pessoas a olhar com mais atenção para o que têm, entender o valor da  <br>
partilha e fazer parte de uma corrente do bem. Porque pequenos gestos, quando movidos pelo amor ao próximo, geram grandes impactos.
</p>

